# Logical Operators ------------------------------------------
# logical operators combine multiple conditions together
# and: both conditions must be True
# or: at least one condition must be True
# not: flips the result (True becomes False, False becomes True)

# And: Both conditions must be True
x = 10
y = 5
result = (x > 5) and (y < 10)
print(result)
# output: True

# Or: At least one condition must be True
a = 3
b = 8
result = (x > 5) or (b > 5)
print(result)
# output: True

# Not: Flips the result
x = 10
y = 5
result = not (x > y)
print(result)
# output: False


# Relational Operators ---------------------------------------
# relational operators compare two values and return True or False

# Equal to: ==
a = 5
b = 5
print(a == b)
# output: True

# Not Equal to: !=
x = 10
y = 3
print(x != y)
# output: True

# Greater than: >
num1 = 7
num2 = 4
print(num1 > num2)
# output: True

# Less than: 
a = 2
b = 8
print(a < b)
# output: True

# Greater than or Equal to: >=
x = 6
y = 6
print(x >= y)
# output: True

# Less than or Equal to: <=
p = 3
q = 5
print(p <= q)
# output: True