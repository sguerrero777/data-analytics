# Brainstorm: How do you convert a temperature from Celsius to Fahrenheit?

# The formula for temperature conversion from Celsius to 
# Fahrenheit is F = (9 / 5) * C + 32. We only need one variable
# for the current Celsius.

# =============================================================

# Final Script for Temperature Conversion

# Define known values
celsius = 37

# Calculate the unknown
fahrenheit = (9 / 5) * celsius + 32

# Display the results
print(f'Celsius is {celsius:.1f} degrees')
print(f'Fahrenheit for {celsius:.1f} degrees is {fahrenheit:.1f} degrees')

# Output 
# Celsius is 37.0 degrees
# Fahrenheit for 37.0 degrees is 98.6 degrees