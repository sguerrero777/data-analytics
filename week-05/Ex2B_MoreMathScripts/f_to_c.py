# Brainstorm: How do you convert a temperature from Fahrenheit to Celsius?

# The formula for temperature conversion from Fahrenheit to 
# Celsius is c = 5 / 9 * (F - 32). We only need one variable
# for the current Fahrenheit.

# =============================================================

# Final Script for Temperature Conversion

# Define known values
fahrenheit = 98

# Calculate the unknown
celsius = 5 / 9 * (fahrenheit - 32)

# Display the results
print(f'Fahrenheit is {fahrenheit:.1f} degrees')
print(f'Celsius for {fahrenheit:.1f} degrees is {celsius:.1f} degrees')

# Output
# Fahrenheit is 98.0 degrees
# Celsius for 98.0 degrees is 36.7 degrees