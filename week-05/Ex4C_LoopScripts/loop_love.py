# Create a list of words that mean great such as amazing, awesome, excellent, etc. 
# Use a for loop to iterate through your list, and with each loop print out the phrase 
# “Loops are ____!” filling in the blank with a word from the list. 
# When your loop has finished, print the additional phrase “I <3 loops” 

