# Final Script for Love Loops

words = ['amazing', 'superb', 'excellent', 'awesome', 'fantastic']

for i in words:
    print(f'Loops are {i}!')
print('I <3 loops')

# Output:
# Loops are amazing!
# Loops are superb!
# Loops are excellent!
# Loops are awesome!
# Loops are fantastic!
# I <3 loops
